#area of square,triangle, rectangle
x=int(input("Enter a side for the square:"))
l=int(input("Enter length of the rectangle:"))
w=int(input("Enter width of the rectangle:"))
B=int(input("Enter base of the triangle:"))
H=int(input("Enter height of the triangle:"))
areaOfSq=x*x
areaOfRec=l*w
areaOfTriangle=(1/2)*B*H

print("area of square=",areaOfSq)
print("area of rectangle=",areaOfRec)
print("area of the triangle=",areaOfTriangle)
